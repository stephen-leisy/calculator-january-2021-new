export function getAddSum(firstNumber, secondNumber) {
    return firstNumber + secondNumber;
}

export function getSubtractSum(firstNumber, secondNumber) {
    return firstNumber - secondNumber;
}

export function getMultiplySum(firstNumber, secondNumber) {
    return firstNumber * secondNumber;
}

export function getDivideSum(firstNumber, secondNumber) {
    return firstNumber / secondNumber;
}